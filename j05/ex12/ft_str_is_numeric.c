int ft_str_is_numeric(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 48 && str[a] <= 57)
		{
			return (1);
		}
		if (str[a] < 48 && str[a] >= 57)
		{
			return (0);
		}
		else
		{
			return (1);
		}
	a++;
	}
}
