int		ft_str_is_lowercase(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 97 && str[a] <= 122)
		{
			return (1);
		}
		if (str[a] <= 97 && str[a] >= 122)
		{
			return (0);
		}
		else
		{
			return (1);
		}
	a++;
	}
}
