int		ft_str_is_printable(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if (str[a] <= 31 || str[a] == 127)
		{
			return (1);
		}
		if (str[a] >= 31 || str[a] != 127)
		{
			return (0);
		}
		else
		{
			return (1);
		}
	a++;
	}
}

